package com.epic.cms.admin.controlpanel.transactionmgt.bean;

/**
 * set currency details to bean
 * @author ayesh
 */
public class CurrencyBean {

    private String currencyCode;
    private String currencyDes;
    private String currencyAlphaCode;
    private String exponent;
    private String lastUpdatedUser;
    
    private String status;
    private String statusDes;   
    

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getStatusDes() {
        return statusDes;
    }

    public void setStatusDes(String statusDes) {
        this.statusDes = statusDes;
    }
    
    public String getLastUpdatedUser() {
        return lastUpdatedUser;
    }

    public void setLastUpdatedUser(String lastUpdatedUser) {
        this.lastUpdatedUser = lastUpdatedUser;
    }

    public String getCurrencyAlphaCode() {
        return currencyAlphaCode;
    }

    public void setCurrencyAlphaCode(String currencyAlphaCode) {
        this.currencyAlphaCode = currencyAlphaCode;
    }

    public String getExponent() {
        return exponent;
    }

    public void setExponent(String exponent) {
        this.exponent = exponent;
    }

    /**
     * get currency code
     * @return string-currencyCode
     */
    public String getCurrencyCode() {
        return currencyCode;
    }

    /**
     * set currency code
     * @param currencyCode-string 
     */
    public void setCurrencyCode(String currencyCode) {
        this.currencyCode = currencyCode;
    }

    /**
     * get currency description
     * @return string-currencyDes
     */
    public String getCurrencyDes() {
        return currencyDes;
    }

    /**
     * set currency description
     * @param currencyDes 
     */
    public void setCurrencyDes(String currencyDes) {
        this.currencyDes = currencyDes;
    }
}
