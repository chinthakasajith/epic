package com.epic.cms.admin.controlpanel.transactionmgt.bean;

import java.util.Date;

/**
 *this class use for store the information about exchange rate
 * @author ayesh
 */
public class ExChangeRateBean {

    private int fromCurrency;
    private int toCurrency;
    private String rate;
    private float invetRate;
    private boolean flag = false;
    private String lastUpdateUser;
    private String fromCurDes;
    private String toCurDes;
    private Date lastUpdateDate;

    public int getFromCurrency() {
        return fromCurrency;
    }

    public void setFromCurrency(int fromCurrency) {
        this.fromCurrency = fromCurrency;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public int getToCurrency() {
        return toCurrency;
    }

    public void setToCurrency(int toCurrency) {
        this.toCurrency = toCurrency;
    }

    public boolean isFlag() {
        return flag;
    }

    public void setFlag(boolean flag) {
        this.flag = flag;
    }

    public float getInvetRate() {
        return invetRate;
    }

    public void setInvetRate(float invetRate) {
        this.invetRate = invetRate;
    }

    public String getLastUpdateUser() {
        return lastUpdateUser;
    }

    public void setLastUpdateUser(String lastUpdateUser) {
        this.lastUpdateUser = lastUpdateUser;
    }

    public String getFromCurDes() {
        return fromCurDes;
    }

    public void setFromCurDes(String fromCurDes) {
        this.fromCurDes = fromCurDes;
    }

    public String getToCurDes() {
        return toCurDes;
    }

    public void setToCurDes(String toCurDes) {
        this.toCurDes = toCurDes;
    }

    public Date getLastUpdateDate() {
        return lastUpdateDate;
    }

    public void setLastUpdateDate(Date lastUpdateDate) {
        this.lastUpdateDate = lastUpdateDate;
    }
}
