package com.epic.cms.admin.controlpanel.profilemgt.bean;

import java.sql.Date;

/**
 *
 * @author mahesh_m
 */
public class ProfileCategoryBean {
    private String profileCategoryCode;
    private String profileCategoryname;
    private String status;
    private String lastUpdateduser;
    private Date lastUpdatedTime;
    private Date createdTime;

    public Date getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(Date createdTime) {
        this.createdTime = createdTime;
    }

    public Date getLastUpdatedTime() {
        return lastUpdatedTime;
    }

    public void setLastUpdatedTime(Date lastUpdatedTime) {
        this.lastUpdatedTime = lastUpdatedTime;
    }

    public String getLastUpdateduser() {
        return lastUpdateduser;
    }

    public void setLastUpdateduser(String lastUpdateduser) {
        this.lastUpdateduser = lastUpdateduser;
    }

    public String getProfileCategoryCode() {
        return profileCategoryCode;
    }

    public void setProfileCategoryCode(String profileCategoryCode) {
        this.profileCategoryCode = profileCategoryCode;
    }

    public String getProfileCategoryname() {
        return profileCategoryname;
    }

    public void setProfileCategoryname(String profileCategoryname) {
        this.profileCategoryname = profileCategoryname;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
}
