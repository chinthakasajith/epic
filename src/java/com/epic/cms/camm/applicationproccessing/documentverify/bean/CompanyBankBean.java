public class CompanyBankBean {
    
    private String bankName;
    private String branchName;
    private String accountType;
    private String accountNo;
    private String accountSinceYear;
    private String accountSinceMonth;

    public String getAccountNo() {
        return accountNo;
    }

    public void setAccountNo(String accountNo) {
        this.accountNo = accountNo;
    }

    public String getAccountSinceMonth() {
        return accountSinceMonth;
    }

    public void setAccountSinceMonth(String accountSinceMonth) {
        this.accountSinceMonth = accountSinceMonth;
    }

    public String getAccountSinceYear() {
        return accountSinceYear;
    }

    public void setAccountSinceYear(String accountSinceYear) {
        this.accountSinceYear = accountSinceYear;
    }

    public String getAccountType() {
        return accountType;
    }

    public void setAccountType(String accountType) {
        this.accountType = accountType;
    }

    public String getBankName() {
        return bankName;
    }

    public void setBankName(String bankName) {
        this.bankName = bankName;
    }

    public String getBranchName() {
        return branchName;
    }

    public void setBranchName(String branchName) {
        this.branchName = branchName;
    }
    
    
}
