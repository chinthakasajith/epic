package com.epic.cms.backoffice.eodfilegeneration.bean;

import java.util.Date;

/**
 *
 * @author chanuka
 */
public class SettlementFileBean {

    private String fileId = null;
    private String fileName = null;
    private String status = null;
    private String statusDes = null;
    private String isTakenToEod = null;
    private String lastUpdatedUser = null;
    private Date lastUpdatedTime = null;
    private Date createdTime = null;

    public String getStatusDes() {
        return statusDes;
    }

    public void setStatusDes(String statusDes) {
        this.statusDes = statusDes;
    }

    
    public String getIsTakenToEod() {
        return isTakenToEod;
    }

    public void setIsTakenToEod(String isTakenToEod) {
        this.isTakenToEod = isTakenToEod;
    }

    
    public Date getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(Date createdTime) {
        this.createdTime = createdTime;
    }

    public String getFileId() {
        return fileId;
    }

    public void setFileId(String fileId) {
        this.fileId = fileId;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public Date getLastUpdatedTime() {
        return lastUpdatedTime;
    }

    public void setLastUpdatedTime(Date lastUpdatedTime) {
        this.lastUpdatedTime = lastUpdatedTime;
    }

    public String getLastUpdatedUser() {
        return lastUpdatedUser;
    }

    public void setLastUpdatedUser(String lastUpdatedUser) {
        this.lastUpdatedUser = lastUpdatedUser;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
