package com.epic.cms.backoffice.installement.btpayment.bean;

import java.util.Date;

/**
 *
 * @author sajith_g
 */
public class BTPaymentPlanBean {
    private String paymentplancode;
    private String description;
    private String duration;
    private String interestrate;
    private String statusCode;
    private String statusDes;
    private String lastUpdatedUser;
    private Date lastUpdatedTime;
    private Date createdTime;

    public String getPaymentplancode() {
        return paymentplancode;
    }

    public void setPaymentplancode(String paymentplancode) {
        this.paymentplancode = paymentplancode;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getInterestrate() {
        return interestrate;
    }

    public void setInterestrate(String interestrate) {
        this.interestrate = interestrate;
    }

    public String getLastUpdatedUser() {
        return lastUpdatedUser;
    }

    public void setLastUpdatedUser(String lastUpdatedUser) {
        this.lastUpdatedUser = lastUpdatedUser;
    }

    public Date getLastUpdatedTime() {
        return lastUpdatedTime;
    }

    public void setLastUpdatedTime(Date lastUpdatedTime) {
        this.lastUpdatedTime = lastUpdatedTime;
    }

    public Date getCreatedTime() {
        return createdTime;
    }

    public void setCreatedTime(Date createdTime) {
        this.createdTime = createdTime;
    }

    public String getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(String statusCode) {
        this.statusCode = statusCode;
    }

    public String getStatusDes() {
        return statusDes;
    }

    public void setStatusDes(String statusDes) {
        this.statusDes = statusDes;
    }
}
