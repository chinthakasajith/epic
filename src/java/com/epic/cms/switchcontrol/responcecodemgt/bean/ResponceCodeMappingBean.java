public class ResponceCodeMappingBean {
    
    private String dbCode;
    private String responceCode;
    private String responceCodeDes;
    private String description;
    private String lastUpdatedUser;

    public String getDbCode() {
        return dbCode;
    }

    public void setDbCode(String dbCode) {
        this.dbCode = dbCode;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLastUpdatedUser() {
        return lastUpdatedUser;
    }

    public void setLastUpdatedUser(String lastUpdatedUser) {
        this.lastUpdatedUser = lastUpdatedUser;
    }

    public String getResponceCode() {
        return responceCode;
    }

    public void setResponceCode(String responceCode) {
        this.responceCode = responceCode;
    }

    public String getResponceCodeDes() {
        return responceCodeDes;
    }

    public void setResponceCodeDes(String responceCodeDes) {
        this.responceCodeDes = responceCodeDes;
    }
    
}
