package com.epic.cms.mtmm.merchantmgt.merchantcustomer.bean;

/**
 *
 * @author nalin
 */
public class MerchantCustomerTransactionBean {
    
    private String merchantCustomerNum;
    private String transactionCode;
    private String lastUpdatedUser;
    private String lastUpdatedTime;
    private String creatDate;

    public String getCreatDate() {
        return creatDate;
    }

    public void setCreatDate(String creatDate) {
        this.creatDate = creatDate;
    }

    public String getLastUpdatedTime() {
        return lastUpdatedTime;
    }

    public void setLastUpdatedTime(String lastUpdatedTime) {
        this.lastUpdatedTime = lastUpdatedTime;
    }

    public String getLastUpdatedUser() {
        return lastUpdatedUser;
    }

    public void setLastUpdatedUser(String lastUpdatedUser) {
        this.lastUpdatedUser = lastUpdatedUser;
    }

    public String getMerchantCustomerNum() {
        return merchantCustomerNum;
    }

    public void setMerchantCustomerNum(String merchantCustomerNum) {
        this.merchantCustomerNum = merchantCustomerNum;
    }

    public String getTransactionCode() {
        return transactionCode;
    }

    public void setTransactionCode(String transactionCode) {
        this.transactionCode = transactionCode;
    }
    
    
}
